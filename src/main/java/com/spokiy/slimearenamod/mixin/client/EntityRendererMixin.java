@Mixin(EntityRenderer.class)
public abstract class EntityRendererMixin<T extends Entity> {

    @Inject(method = "renderLabelIfPresent", at = @At("HEAD"), cancellable = true)
    private void onRenderLabel(T entity, Text text, MatrixStack matrices, VertexConsumerProvider vertexConsumers, int light, float tickDelta, CallbackInfo ci) {
        ClientPlayerEntity player = MinecraftClient.getInstance().player;
        ClientWorld world = MinecraftClient.getInstance().world;
        if (player == null || world == null) return;

        if (!canSee(world, player, entity)) {
            ci.cancel();
        }
    }

    @Unique
    private boolean canSee(ClientWorld world, ClientPlayerEntity player, Entity entity) {
        Vec3d from = player.getCameraPosVec(1.0F);
        Vec3d to = entity.getPos().add(0, entity.getHeight() * 0.5, 0);

        HitResult hit = world.raycast(new RaycastContext(
                from,
                to,
                RaycastContext.ShapeType.COLLIDER,
                RaycastContext.FluidHandling.NONE,
                player
        ));

        return hit.getType() == HitResult.Type.MISS;
    }

}